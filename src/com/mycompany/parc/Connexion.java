    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String matricule = jFormattedTextField1.getText();
        String password = this.jPasswordField1.getText();
        if (matricule.equals("") || password.equals("")){
            JOptionPane.showMessageDialog(this.rootPane,
                "Error Matricule ou Mot de passe incorrect","",
                JOptionPane.ERROR_MESSAGE);
        }else{
            int res = 0 ;
            if (matricule.equals("admin") && password.equals("admin"))
                res = 1 ;
            
            if (res == 1){
                Dashbord frame = new Dashbord();
                this.setVisible(false);
                frame.setVisible(true);
                frame.setTitle("Admin Panel");

            }else{
                JOptionPane.showMessageDialog(this.rootPane,
                    "Connexion Failed","",
                    JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

